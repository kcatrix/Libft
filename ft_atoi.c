#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	signe;
	int	result;
	int	x;

	i = 0;
	signe = 1;
	x = 0;
	result = 0;
	while (str[i] == 32 || (str[i] >= 9 && (str[i] <= 13)))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signe *= -1;
		i++;
	}
	if (!(str[i] >= '0' && str[i] <= '9'))
		return (0);
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - '0');
		i++;
	}
	return (result * signe);
}
