#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	int	i;
	char	*s2;

	i = 0;
	while (s1[i] != '\0')
	{
		i++;
	}
	s2 = malloc(i * sizeof(char) + 1);
	i = 0;
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	return(s2);
}
